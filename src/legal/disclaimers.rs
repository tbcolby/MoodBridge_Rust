/// Helper struct for managing disclaimers
#[derive(Debug, Serialize, Deserialize)]
pub struct DisclaimerManager {
    pub disclaimers_shown: std::collections::HashMap<String, Vec<String>>,
    pub acknowledgments_required: Vec<String>,
}

impl DisclaimerManager {
    pub fn new() -> Self {
        Self {
            disclaimers_shown: std::collections::HashMap::new(),
            acknowledgments_required: Vec::new(),
        }
    }

    /// Record that a user has been shown a specific disclaimer
    pub fn record_disclaimer_shown(&mut self, user_id: &str, disclaimer_type: &str) {
        self.disclaimers_shown
            .entry(user_id.to_string())
            .or_insert_with(Vec::new)
            .push(disclaimer_type.to_string());
    }

    /// Check if a user has acknowledged a specific disclaimer
    pub fn has_acknowledged_disclaimer(&self, user_id: &str, disclaimer_type: &str) -> bool {
        self.disclaimers_shown
            .get(user_id)
            .map(|disclaimers| disclaimers.contains(&disclaimer_type.to_string()))
            .unwrap_or(false)
    }

    /// Get the appropriate disclaimer text for an operation
    pub fn get_disclaimer(&self, operation_type: &str) -> &'static str {
        match operation_type {
            "ai_legal_advice" => AI_LEGAL_ADVICE_DISCLAIMER,
            "voice_recording" => VOICE_RECORDING_DISCLAIMER,
            "document_modification" => DOCUMENT_MODIFICATION_DISCLAIMER,
            "client_data" => CLIENT_DATA_DISCLAIMER,
            "presentation_generation" => PRESENTATION_GENERATION_DISCLAIMER,
            "semantic_search" => SEMANTIC_SEARCH_DISCLAIMER,
            "collaboration_metrics" => COLLABORATION_METRICS_DISCLAIMER,
            "timeline_analysis" => TIMELINE_ANALYSIS_DISCLAIMER,
            _ => GENERAL_DISCLAIMER,
        }
    }
}
